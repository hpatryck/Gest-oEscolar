    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/escolar", "root", "");
            String sql = "insert into cadastro_estudante values(?,?,?,?,?,?,?,?)";
            
            PreparedStatement ptstmt = (PreparedStatement) conn.prepareStatement(sql);
                ptstmt.setString(1,id.getText());
                ptstmt.setString(2,nome.getText());
                ptstmt.setString(3,nomeresp.getText());
                ptstmt.setString(4,tel.getText());
                ptstmt.setString(5,endereco.getText());
                ptstmt.setString(6,bairro.getText());
                ptstmt.setString(7,cidade.getText());
                ptstmt.setString(8,turma.getText());
            JOptionPane.showMessageDialog(null, "CADASTRO REALIZADO COM SUCESSO!");
            conn.close();
               
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

