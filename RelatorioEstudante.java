    @SuppressWarnings("null")
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try{
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/escolar", "root", "");
            String sql = "insert into relatorio_estudante values(?,?,?,?,?,?,?";
            
            String p = portugues.getText();
            double s1 = Double.parseDouble(p);
            String m = matematica.getText();
            double s2 = Double.parseDouble(m);
            String c = ciencias.getText();
            double s3 = Double.parseDouble(c);
            
            double total = (s1 + s2 + s3)/3;
            
            String situacao;
            
            if(total >= 7){
                situacao = "Aprovado";
            }else{
                situacao = "Reprovado";
            }
          
            String id = null;
            String n = nome.getText();
            String t = turma.getText();
            String por = portugues.getText();
            String mat = matematica.getText();
            String cie = ciencias.getText();
            
            PreparedStatement ptstmt = (PreparedStatement)conn.prepareStatement(sql);
            ptstmt.setString(1, id);
            ptstmt.setString(2, n);
            ptstmt.setString(3, t);
            ptstmt.setString(4, por);
            ptstmt.setString(5, mat);
            ptstmt.setString(6, cie);
            ptstmt.setString(7, situacao);   
            
            ptstmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "CADASTRO REALIZADO COM SUCESSO!");
            
            textrel.setText("Relatorio\n\n Nome:"+n+"\nTurma:"+t+"\n\n-----------\nDisciplinas:\n\nPortugues:"+por+"\nMatematica:"+mat+"\nCiencias"+cie+"\nSituacao:"+situacao);
            conn.close();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

